#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	unsigned char	*tmp_pointer;

	tmp_pointer = (unsigned char *)s;
	while (n > 0)
	{
		*tmp_pointer = 0;
		tmp_pointer++;
		n--;
	}
}
